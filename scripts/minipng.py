def _glyph(c):
    return _F.get(c.upper(), _F['?'] if '?' in _F else _F[' '])

class Canvas:
    def __init__(self, w, h, bg=(255,255,255)):
        self.w, self.h = w, h
        self.buf = bytearray(bytes(bg)*(w*h))
    def px(self, x, y, col):
        x=int(x); y=int(y)
        if 0<=x<self.w and 0<=y<self.h:
            i=(y*self.w+x)*3
            self.buf[i:i+3]=bytes(col)
    def rect(self, x, y, w, h, col):
        for yy in range(int(y), int(y+h)):
            for xx in range(int(x), int(x+w)):
                self.px(xx,yy,col)
    def disc(self, cx, cy, r, col):
        for yy in range(int(cy-r), int(cy+r+1)):
            for xx in range(int(cx-r), int(cx+r+1)):
                if (xx-cx)**2+(yy-cy)**2 <= r*r:
                    self.px(xx,yy,col)
    def line(self, x0,y0,x1,y1,col,t=1):
        x0,y0,x1,y1=int(x0),int(y0),int(x1),int(y1)
        dx=abs(x1-x0); dy=-abs(y1-y0)
        sx=1 if x0<x1 else -1; sy=1 if y0<y1 else -1
        err=dx+dy; r=t//2
        while True:
            if t<=1: self.px(x0,y0,col)
            else: self.disc(x0,y0,r,col)
            if x0==x1 and y0==y1: break
            e2=2*err
            if e2>=dy: err+=dy; x0+=sx
            if e2<=dx: err+=dx; y0+=sy
    def text(self, x, y, s, col, scale=2):
        cx=x
        for ch in s:
            g=_glyph(ch)
            for ry,row in enumerate(g):
                for rxi,bit in enumerate(row):
                    if bit=='1':
                        self.rect(cx+rxi*scale, y+ry*scale, scale, scale, col)
            cx += (5+1)*scale
        return cx
    def text_w(self, s, scale=2):
        return len(s)*(5+1)*scale
    def text_center(self, cx, y, s, col, scale=2):
        self.text(cx-self.text_w(s,scale)/2, y, s, col, scale)
    def text_right(self, xr, y, s, col, scale=2):
        self.text(xr-self.text_w(s,scale), y, s, col, scale)
    def save(self, path):
        def chunk(typ,data):
            return (struct.pack(">I",len(data))+typ+data+
                    struct.pack(">I",zlib.crc32(typ+data)&0xffffffff))
        raw=bytearray(); stride=self.w*3
        for y in range(self.h):
            raw.append(0); raw+=self.buf[y*stride:(y+1)*stride]
        with open(path,"wb") as f:
            f.write(b"\x89PNG\r\n\x1a\n")
            f.write(chunk(b"IHDR",struct.pack(">IIBBBBB",self.w,self.h,8,2,0,0,0)))
            f.write(chunk(b"IDAT",zlib.compress(bytes(raw),9)))
            f.write(chunk(b"IEND",b""))
